Objetivos de Desenvolvimento Sustentável até 2030 da ONU. 
<br>
<h3>- Ela destaca a necessidade de colaboração
 entre governos, setor privado, ONGs, academia e comunidades locais para espalhar os efeitos do 
 desenvolvimento sustentável.</h3> 
 <br>
